@pytest.fixture(scope="module")
def course_id(client, sample_pdf) -> Generator[str, None, None]:
    """
    Fixture: upload file PDF thật lên backend, trả về course_id.
    Tự động cleanup sau khi test xong.
    """
    # Upload
    response = client.post(
        "/api/upload",
        files={"file": ("test_sample.pdf", sample_pdf, "application/pdf")},
    )
    assert response.status_code == 200
    data = response.json()
    cid = data["course_id"]

    # Đợi processing hoàn tất (poll tối đa 30s)
    timeout = 30
    interval = 2
    elapsed = 0
    while elapsed < timeout:
        status_resp = client.get(f"/api/course/{cid}/status")
        if status_resp.status_code == 200:
            status = status_resp.json().get("status")
            if status == "ready":
                break
        time.sleep(interval)
        elapsed += interval

    yield cid

    # Cleanup: xóa course
    try:
        client.delete(f"/courses/{cid}")
    except Exception:
        pass